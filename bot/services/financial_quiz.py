def public_questions() -> list[dict]:
    """Вопросы без правильных ответов — то, что можно безопасно отдавать клиенту."""
    return [{"text": q["text"], "options": q["options"]} for q in QUIZ_QUESTIONS]


def score_quiz(answers: list[int]) -> tuple[int, int]:
    """Возвращает (кол-во правильных, всего вопросов). Некорректные/отсутствующие ответы = неверно."""
    total = len(QUIZ_QUESTIONS)
    correct = 0
    for i, question in enumerate(QUIZ_QUESTIONS):
        if i < len(answers) and answers[i] == question["correct_index"]:
            correct += 1
    return correct, total
