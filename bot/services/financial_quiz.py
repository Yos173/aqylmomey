def public_questions() -> list[dict]:
    """Вопросы без правильных ответов — то, что можно безопасно отдавать клиенту."""
    return [{"text": q["text"], "options": q["options"]} for q in QUIZ_QUESTIONS]


def score_quiz(answers: list[int]) -> tuple[int, int]:
    """Возвращает (кол-во правильных, всего вопросов). Некорректные/отсутствующие ответы = неверно."""
    total = len(QUIZ_QUESTIONS)
    correct = 0
    for i, question in enumerate(QUIZ_QUESTIONS):
        if i < len(answers) and answers[i] == question["correct_index"]:
            correct += 1
    return correct, total


def review_quiz(answers: list[int]) -> list[dict]:
    """Постатейный разбор: правильный ответ и объяснение для каждого вопроса, где пользователь ошибся.

    Отдаётся только после отправки квиза — до этого правильные ответы клиенту не передаются.
    """
    mistakes = []
    for i, question in enumerate(QUIZ_QUESTIONS):
        given = answers[i] if i < len(answers) else None
        if given == question["correct_index"]:
            continue
        mistakes.append(
            {
                "question": question["text"],
                "your_answer": question["options"][given] if given is not None and 0 <= given < len(question["options"]) else None,
                "correct_answer": question["options"][question["correct_index"]],
                "explanation": question["explanation"],
            }
        )
    return mistakes
